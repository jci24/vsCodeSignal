import * as React from 'react'

import { cn } from '@/shared/lib/cn'

const Card = React.forwardRef<HTMLDivElement, React.HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn('rounded-3xl bg-card text-card-foreground', className)}
      {...props}
    />
  ),
)

